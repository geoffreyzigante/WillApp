/**
 * Expo Config Plugin: with-photo-filter
 *
 * Injecte un petit module natif iOS (WillPhotoFilter) qui evalue une photo
 * capturee via le framework Apple Vision et renvoie {accepted, hasFace,
 * faceWidthPx, faceCaptureQuality, reason} au JS. Sert au post-capture filter
 * du mode photographe (mode single-shot HQ) : on rejette les photos sans
 * visage, trop petites, ou de qualite Vision < seuil.
 *
 * Pourquoi un patch dans node_modules/react-native-vision-camera plutot qu'un
 * nouveau pod : la subspec `React` de VisionCamera inclut deja les .swift +
 * .{h,m} de ios/React/ et depend de React-Core (RCTBridgeModule dispo). On
 * profite de l'autolinking existant, sans avoir a creer un pod separe ni a
 * modifier le Podfile/Xcode project.
 *
 * Fichiers ecrits (idempotent : skip si deja presents et a jour) :
 *  - node_modules/react-native-vision-camera/ios/React/WillPhotoFilter.swift
 *  - node_modules/react-native-vision-camera/ios/React/WillPhotoFilter.m
 *
 * Exposition JS :
 *   NativeModules.WillPhotoFilter.evaluate(path, minFaceWidthPx, minQuality)
 *   -> Promise<{ accepted, hasFace, faceWidthPx, faceCaptureQuality, reason }>
 *
 * IMPORTANT: ce patch ajoute du Swift + ObjC, il faut un nouveau eas build —
 * pas d'OTA possible.
 */

const { withDangerousMod } = require('@expo/config-plugins');
const fs = require('fs');
const path = require('path');

const REACT_DIR_REL =
  'node_modules/react-native-vision-camera/ios/React';
const SWIFT_FILE = 'WillPhotoFilter.swift';
const OBJC_FILE = 'WillPhotoFilter.m';

const SWIFT_SOURCE = `//
//  WillPhotoFilter.swift
//  VisionCamera (WillApp post-capture filter)
//
//  Auto-generated by plugins/with-photo-filter.js — do not edit by hand.
//
//  Evaluates a captured photo via Apple Vision framework and returns a
//  verdict to JS (accept / reject + reason). Used by the photographer mode
//  single-shot HQ pipeline to discard blurry / faceless / too-small photos
//  before they enter the upload queue.
//
//  Vision request : VNDetectFaceCaptureQualityRequest, available since iOS 13.
//  This request returns face bounding boxes + a faceCaptureQuality score in
//  [0..1] that combines sharpness, lighting, expression and pose. It's a
//  better proxy than raw Laplacian-based sharpness because it focuses on the
//  face region (where it matters for race photos).
//

import Foundation
import Vision
import UIKit

@objc(WillPhotoFilter)
final class WillPhotoFilter: NSObject {

  @objc
  static func requiresMainQueueSetup() -> Bool {
    return false
  }

  // Called from JS as:
  //   await NativeModules.WillPhotoFilter.evaluate(path, minFaceWidthPx, minQuality)
  // - path: absolute file URL (with or without file:// prefix) to the JPEG.
  // - minFaceWidthPx: int, the largest face must be >= this width (pixels).
  // - minQuality: float in [0..1], the largest face must have a Vision
  //   faceCaptureQuality >= this value.
  // Resolves with a dict; never rejects on "rejected by filter" — only on
  // hard errors (image load failure, Vision crash). The JS layer decides
  // what to do with the verdict.
  @objc
  func evaluate(_ pathArg: NSString,
                minFaceWidthPx: NSNumber,
                minQuality: NSNumber,
                resolver: @escaping RCTPromiseResolveBlock,
                rejecter: @escaping RCTPromiseRejectBlock) {
    let raw = pathArg as String
    let cleaned: String
    if raw.hasPrefix("file://") {
      cleaned = String(raw.dropFirst("file://".count))
    } else {
      cleaned = raw
    }

    // Off the main queue : Vision is CPU-heavy, ne pas bloquer.
    DispatchQueue.global(qos: .userInitiated).async {
      guard let image = UIImage(contentsOfFile: cleaned),
            let cgImage = image.cgImage else {
        rejecter("E_LOAD",
                 "WillPhotoFilter: could not load image at \\(cleaned)",
                 nil)
        return
      }

      let imageWidth = CGFloat(cgImage.width)
      let imageHeight = CGFloat(cgImage.height)
      let orientation = Self.cgOrientation(from: image.imageOrientation)
      let handler = VNImageRequestHandler(cgImage: cgImage,
                                          orientation: orientation,
                                          options: [:])
      let request = VNDetectFaceCaptureQualityRequest()

      do {
        try handler.perform([request])
      } catch {
        rejecter("E_VISION",
                 "WillPhotoFilter: Vision request failed: \\(error.localizedDescription)",
                 error as NSError)
        return
      }

      let observations = (request.results as? [VNFaceObservation]) ?? []
      if observations.isEmpty {
        resolver([
          "accepted": false,
          "hasFace": false,
          "faceWidthPx": 0,
          "faceCaptureQuality": NSNull(),
          "reason": "no_face",
        ])
        return
      }

      // Plus grand visage (par aire) parmi les detections. Sur les photos
      // de course on peut avoir plusieurs visages (peloton) ; on evalue
      // celui qui interesse le plus le photographe (le sujet principal).
      var best: VNFaceObservation = observations[0]
      var bestArea: CGFloat = 0
      for obs in observations {
        let w = obs.boundingBox.width * imageWidth
        let h = obs.boundingBox.height * imageHeight
        let area = w * h
        if area > bestArea {
          bestArea = area
          best = obs
        }
      }
      let faceWidthPx = Int(best.boundingBox.width * imageWidth)
      // faceCaptureQuality est optionnel : nil sur ancien iOS ou sur certains
      // formats. On traite nil comme "qualite inconnue" -> on accepte plutot
      // que rejeter, pour ne pas trasher des photos a cause d'un detail Vision.
      let qualityOpt = best.faceCaptureQuality
      let minWidth = minFaceWidthPx.intValue
      let minQ = minQuality.floatValue
      var accepted = true
      var reason = "ok"
      if faceWidthPx < minWidth {
        accepted = false
        reason = "face_too_small"
      } else if let q = qualityOpt, Float(q) < minQ {
        accepted = false
        reason = "low_quality"
      }
      resolver([
        "accepted": accepted,
        "hasFace": true,
        "faceWidthPx": faceWidthPx,
        "faceCaptureQuality": qualityOpt.map { NSNumber(value: Float($0)) } ?? NSNull(),
        "reason": reason,
      ])
    }
  }

  // UIImage.Orientation -> CGImagePropertyOrientation. Vision a besoin de
  // l'orientation EXIF pour ne pas detecter les visages 90deg de cote.
  private static func cgOrientation(from ui: UIImage.Orientation) -> CGImagePropertyOrientation {
    switch ui {
    case .up: return .up
    case .down: return .down
    case .left: return .left
    case .right: return .right
    case .upMirrored: return .upMirrored
    case .downMirrored: return .downMirrored
    case .leftMirrored: return .leftMirrored
    case .rightMirrored: return .rightMirrored
    @unknown default: return .up
    }
  }
}
`;

const OBJC_SOURCE = `//
//  WillPhotoFilter.m
//  VisionCamera (WillApp post-capture filter)
//
//  Auto-generated by plugins/with-photo-filter.js — do not edit by hand.
//
//  Obj-C bridge exposing the Swift WillPhotoFilter class to React Native
//  via NativeModules.WillPhotoFilter. The VisionCamera React subspec already
//  includes ios/React/**/*.{h,m} so this file is picked up automatically by
//  CocoaPods at install time.
//

#import <Foundation/Foundation.h>
#import <React/RCTBridgeModule.h>

@interface RCT_EXTERN_MODULE(WillPhotoFilter, NSObject)

RCT_EXTERN_METHOD(evaluate:(NSString *)path
                  minFaceWidthPx:(nonnull NSNumber *)minFaceWidthPx
                  minQuality:(nonnull NSNumber *)minQuality
                  resolver:(RCTPromiseResolveBlock)resolver
                  rejecter:(RCTPromiseRejectBlock)rejecter)

+ (BOOL)requiresMainQueueSetup
{
  return NO;
}

@end
`;

function writeIfChanged(filePath, content) {
  if (fs.existsSync(filePath)) {
    const existing = fs.readFileSync(filePath, 'utf8');
    if (existing === content) {
      console.log(`[with-photo-filter] ${path.basename(filePath)} unchanged`);
      return false;
    }
  }
  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`[with-photo-filter] wrote ${path.basename(filePath)}`);
  return true;
}

function installFiles(projectRoot) {
  const reactDir = path.join(projectRoot, REACT_DIR_REL);
  if (!fs.existsSync(reactDir)) {
    throw new Error(
      `[with-photo-filter] Dossier introuvable : ${reactDir}. ` +
        'react-native-vision-camera est-il installe ?'
    );
  }
  writeIfChanged(path.join(reactDir, SWIFT_FILE), SWIFT_SOURCE);
  writeIfChanged(path.join(reactDir, OBJC_FILE), OBJC_SOURCE);
}

module.exports = function withPhotoFilter(config) {
  return withDangerousMod(config, [
    'ios',
    async (cfg) => {
      installFiles(cfg.modRequest.projectRoot);
      return cfg;
    },
  ]);
};
